class Product {
  final String id;
  final String productName;
  final String productDetails;
  final String image;
  final double price;
  final int quantity;
  final String sellerName;

  Product({
    required this.id,
    required this.productName,
    required this.productDetails,
    required this.image,
    required this.price,
    required this.quantity,
    required this.sellerName,
  });
}
